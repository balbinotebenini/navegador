    private void jButtonLimpaTudoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLimpaTudoActionPerformed
        try {
            for (int i = 0; i < ListaConsulta.aListFavorito.size(); i++) {
                Historico h = new Historico();
                h.RemoveFavoritoTodos(webbrowser.Classes.UsuarioLogado.getUsuarioLogado());
                FavoritoTable.DeleteAllRowsInTable();
                carrega();
            }
        } catch (Exception ex) {
            Logger.getLogger(TabFavorito.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonLimpaTudoActionPerformed

    private void jButtonExcluiSelecionadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluiSelecionadoActionPerformed

        try {
            Historico x = new Historico();
            String h;
            h = FavoritoTable.getColunaSelecionada(jTableFavorito.getSelectedRow());
            x.RemoveFavoritoSelecionado(h, webbrowser.Classes.UsuarioLogado.getUsuarioLogado());
            FavoritoTable.DeleteRowInTable(h);
            carrega();
        } catch (Exception ex) {
            Logger.getLogger(TabFavorito.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonExcluiSelecionadoActionPerformed

