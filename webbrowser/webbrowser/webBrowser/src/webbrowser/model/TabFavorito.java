    //carrega dados na tabela
    public void carrega() {
        FazConsulta f = new FazConsulta();

        try {
            f.fazConsultaFavoritoSQL("select distinct UrlAcesso from historico where usuarioAcesso = '"
                    + webbrowser.Classes.UsuarioLogado.getUsuarioLogado()
                    + "' and favoritoAcesso = true");
        } catch (SQLException ex) {
            Logger.getLogger(TabFavorito.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (jTableFavorito.getRowCount() > 0) {
            System.out.println("tem linhas");
            FavoritoTable.DeleteAllRowsInTable();
        }

        for (String aList : ListaConsulta.aListFavorito) {
            FavoritoTable.addRowInTable(aList.toString());
        }
    }

    public void apaga() {

        if (jTableFavorito.getRowCount() > 0) {
            do {
                ((DefaultTableModel) jTableFavorito.getModel()).removeRow(0);
            } while (jTableFavorito.getRowCount() > 0);
        }
    }

